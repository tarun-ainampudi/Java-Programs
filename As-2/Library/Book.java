public class Book{
    String BookId;
    String BookName;
    String AuthorName;
    String PublisherName;
    double Price;
    public Book(String BookId,String BookName,String AuthorName,String PublisherName,double Price){
        this.BookId = BookId;
        this.BookName = BookName;
        this.AuthorName = AuthorName;
        this.PublisherName = PublisherName;
        this.Price = Price;
    }
}