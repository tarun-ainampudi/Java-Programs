public class LibraryUser {
    String UserId;
    String UserName;
    String UserAddress;
    double ContactNumber;
    public LibraryUser(String UserId,String UserName,String UserAddress,double ContactNumber){
        this.UserId = UserId;
        this.UserName = UserName;
        this.UserAddress = UserAddress;
        this.ContactNumber = ContactNumber;
    }
}
